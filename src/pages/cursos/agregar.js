import Head from "next/head";
import { useState, useEffect } from "react";
import { useRouter } from "next/router";
import { addItemToLocalStorage } from "@/utils/storage";

export default function Home() {
  const router = useRouter();
    const [curso, setCurso] = useState('');

  const addCurso = async () => {
    addItemToLocalStorage('cursos', curso)
    await router.push("/cursos");
  }

  return (
    <>
      <Head>
        <title>Proyecto de IS2 - Fase 3</title>
        <meta name="description" content="Generated by Proyecto de IS2" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="container">
        <div className="form-group">
          <label htmlFor="title">Curso:</label>
          <input
            type="text"
            className="form-control my-3"
            id="title"
            placeholder="Curso"
            value={curso}
            onChange={(e) => setCurso(e.target.value)}
          />
        </div>
        <button type="submit" className="btn btn-primary" onClick={addCurso}>
          Agregar
        </button>
      </main>
    </>
  );
}
